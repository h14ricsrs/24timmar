    //Hjälpmedtod, printar ut arrayen
    private void fyllTextArea() {

        taRegister.setText(null);

        for (Bakverk temp : bakelseArray) {

            if (temp != null) {

                taRegister.append(temp.getBakverk());
            }
        }
    }

    private void antalKanelBulle() {
        int antal = 0;

        for (Bakverk temp : bakelseArray) {
            if (temp != null) {
                if (temp.getProduktTyp().equalsIgnoreCase("kanelbulle")) {
                    antal++;
                }
            }
        }
        lblKanelbulle.setText("Antal med produkttyp Kanelbulle: " + antal);
    }

    private void antalSemla() {
        int antal = 0;

        for (Bakverk temp : bakelseArray) {
            if (temp != null) {
                if (temp.getProduktTyp().equalsIgnoreCase("semla")) {
                    antal++;
                }
            }
        }
        lblSemla.setText("Antal med produkttyp Semla: " + antal);
    }

    private void antalBakelser() {
        int antal = 0;
        for (Bakverk temp : bakelseArray) {
            if (temp != null) {
                antal += temp.getAntal();
            }
        }
        lblAntalBakelser.setText("Antal bakverk i lager: " + antal + "st");
    }

    private void totalPris() {

        int pris = 0;
        for (Bakverk temp : bakelseArray) {
            if (temp != null) {

                for (int i = 0; i < temp.getAntal(); i++) {
                    pris += temp.getPris();
                }

            }
        }
        lblTotalPris.setText("Totalt pris för alla bakelser: " + pris + "kr");
    }

