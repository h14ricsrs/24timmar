    private void btnSparaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSparaActionPerformed
        String produktTyp = tfProduktTyp.getText();
        String marke = tfMarke.getText();
        int pris = Integer.parseInt(tfPris.getText());
        int antal = Integer.parseInt(tfAntal.getText());
        String bakeOff = tfBakeOff.getText();
        String storlek = tfStorlek.getText();
        int vikt = Integer.parseInt(tfVikt.getText());
        int funnenProduktId = 0;

        Bakverk bakelse = new Bakverk(produktTyp, marke, pris, antal, bakeOff, storlek, vikt);
        String produktId = bakelse.getProduktId();

        for (Bakverk temp : bakelseArray) {
            if (temp != null && produktId.equalsIgnoreCase(temp.getProduktId())) {
                funnenProduktId++;

            }
        }

        for (int i = 0; i < bakelseArray.length; i++) {
            if (bakelseArray[i] == null && funnenProduktId == 0) {
                bakelseArray[i] = bakelse;
                break;
            }
        }

        tfProduktId.setText(produktId);
        if (funnenProduktId > 0) {
            taRegister.setText("ProduktId: " + produktId + " finns redan registrerad, var vänlig registrera annan produkt.");
        } else {
            fyllTextArea();
        }

    }//GEN-LAST:event_btnSparaActionPerformed

