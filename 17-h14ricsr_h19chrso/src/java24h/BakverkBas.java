    private void btnSokProduktMarkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSokProduktMarkeActionPerformed
        String finnMarke = tfSokMarke.getText();
        String finnProduktTyp = tfSokProduktTyp.getText();
        taRegister.setText(null);

        for (Bakverk temp : bakelseArray) {
            if (temp != null) {
                if (temp.getProduktTyp().equalsIgnoreCase(finnProduktTyp) || temp.getMarke().equalsIgnoreCase(finnMarke)) {
                    taRegister.append(temp.getBakverk());
                }
            }
        }
    }//GEN-LAST:event_btnSokProduktMarkeActionPerformed

